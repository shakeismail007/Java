public  class SwitchEx {
    public static void main(String args[]){
        int a = 6;

        switch (a){
            case 6:
            case 0:
                System.out.println("case 0");
                break;
            case 1:
                System.out.println("case 1");
                break;
            case 2:
                System.out.println("case 1");
                break;
            default:
                System.out.println("default");
                break;
        }

    }
}